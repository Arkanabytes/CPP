#include <iostream>
#include <math.h>
using namespace std;
int main() { 
int base,altura; 
  cout<<"Ingrese la base del rectangulo: "; 
  cin>>base; 
  cout<<"Ingrese la altura del rectangulo: "; 
  cin>>altura; 
  cout<<"El area del rectangulo es: "<<base*altura <<endl; 
  cout<<"\nEl perimetro del rectangulo es: "<<base*2+altura*2 <<endl; 
return 0;
}
	
